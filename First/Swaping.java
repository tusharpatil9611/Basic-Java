// Program to swaping two  variable
package First;
public class Swaping {
public static void main(String[] args){
	int a=4;
	int b=10;
	int c=a;
	System.out.println("Before swap");
	System.out.println(a);
	System.out.println(b);
	a=b;
	b=c;
	c=a;
	System.out.println("Aftet swap");
	System.out.println(a);
	System.out.println(b);
}
}
